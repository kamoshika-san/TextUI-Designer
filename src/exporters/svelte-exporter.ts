import type { TextUIDSL } from '../renderer/types';
import type { ExportOptions, Exporter } from './index';
import { HtmlExporter } from './html-exporter';

function extractContainerContent(html: string): string {
  const matched = html.match(/<div class="container mx-auto p-6">([\s\S]*?)<\/div>\s*<\/body>/i);
  if (!matched) {
    return '';
  }
  return matched[1]
    .trim()
    .split('\n')
    .map(line => line.trimEnd())
    .join('\n');
}

function indent(content: string, spaces: number): string {
  const pad = ' '.repeat(spaces);
  return content
    .split('\n')
    .map(line => `${pad}${line}`)
    .join('\n');
}

export class SvelteExporter implements Exporter {
  async export(dsl: TextUIDSL, _options: ExportOptions): Promise<string> {
    const html = await new HtmlExporter().export(dsl, { format: 'html' });
    const fragment = extractContainerContent(html);
    const markupBody = fragment ? `\n${indent(fragment, 2)}\n` : '\n';

    return `<script lang="ts">
  // Generated by TextUI Designer.
</script>

<main class="textui-generated">
${markupBody}</main>

<style>
  /* Tailwindクラスを利用するため、利用側でTailwindを有効化してください。 */
</style>
`;
  }

  getFileExtension(): string {
    return '.svelte';
  }
}
