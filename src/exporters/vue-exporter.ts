import type { TextUIDSL } from '../domain/dsl-types';
import type { ExportOptions, Exporter } from './export-types';
import { HtmlExporter } from './html-exporter';
import { indentBlock, trimTrailingWhitespace } from './framework-exporter-markup';
import { buildScriptExtensionPointComments } from './framework-exporter-script-extension-points';
import { buildThemeVariables } from './theme-definition-resolver';
import { buildThemeStyleBlock } from './theme-style-builder';
import { readWebviewCssIfPresent } from './html-template-builder';

function buildScriptSetupSection(options: ExportOptions): string {
  if (options.includeComments === false) {
    return `<script setup lang="ts">
// Generated by TextUI Designer.
</script>`;
  }

  return `<script setup lang="ts">
// Generated by TextUI Designer.
${buildScriptExtensionPointComments('vue')}
</script>`;
}

function buildStyleSection(options: ExportOptions): string {
  let themeStyles = '';
  if (options.themePath) {
    try {
      const allVars = buildThemeVariables(options.themePath);
      themeStyles = buildThemeStyleBlock(allVars);
    } catch (error) {
      console.warn(`[VueExporter] テーマ読み込みに失敗しました: ${error instanceof Error ? error.message : String(error)}`);
    }
  }

  const webviewCss = readWebviewCssIfPresent(options.extensionPath) || '';

  return `<style scoped>
  /* Tailwind classes are preserved in the generated markup. Enable Tailwind in the consuming app when you want those classes to take effect. */
${webviewCss}
${themeStyles}
</style>`;
}

function extractContainerContent(html: string): string {
  const matched = html.match(/<body\b[^>]*>\s*<div\b[^>]*>([\s\S]*?)<\/div>\s*<\/body>/i);
  if (!matched) {
    return '';
  }

  return trimTrailingWhitespace(matched[1]);
}

function buildTemplateSection(html: string): string {
  const fragment = extractContainerContent(html);
  const templateBody = fragment ? `\n${indentBlock(fragment, 4)}\n` : '\n';

  return `<template>
  <main class="textui-generated">
${templateBody}  </main>
</template>`;
}

export class VueExporter implements Exporter {
  async export(dsl: TextUIDSL, options: ExportOptions): Promise<string> {
    const html = await new HtmlExporter().export(dsl, { ...options, format: 'html' });

    return `${buildTemplateSection(html)}

${buildScriptSetupSection(options)}

${buildStyleSection(options)}
`;
  }

  getFileExtension(): string {
    return '.vue';
  }
}
