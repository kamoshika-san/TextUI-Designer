/**
 * generate-diff-traceability.cjs
 *
 * Generates a diff-traceability matrix linking rule families → fixtures → tests.
 *
 * Input A: docs/diff-*.md files — scans for `## Rule: {rule_id}` headings
 * Input B: tests/fixtures/diff-acceptance/index.json — fixture index from T-20260331-408
 * Output:  docs/generated/diff-traceability-matrix.md
 *
 * Usage: node scripts/generate-diff-traceability.cjs
 */

'use strict';

const fs = require('fs');
const path = require('path');

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

const ROOT = path.resolve(__dirname, '..');
const DOCS_DIR = path.join(ROOT, 'docs');
const FIXTURE_INDEX = path.join(ROOT, 'tests', 'fixtures', 'diff-acceptance', 'index.json');
const OUTPUT_DIR = path.join(DOCS_DIR, 'generated');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'diff-traceability-matrix.md');

// ---------------------------------------------------------------------------
// Step 1: Extract rule IDs from docs/diff-*.md
// ---------------------------------------------------------------------------

/**
 * Scans all diff-*.md files in DOCS_DIR and extracts rule IDs from
 * `## Rule: {rule_id}` headings (with optional trailing whitespace/text).
 *
 * Returns an array of { ruleId, docFile, anchor } sorted by ruleId.
 */
function extractRuleIdsFromDocs() {
  const rulePattern = /^##\s+Rule:\s+(\S+)/im;
  const rulePatternGlobal = /^##\s+Rule:\s+(\S+)/gim;

  const entries = [];

  const docFiles = fs.readdirSync(DOCS_DIR)
    .filter(f => f.startsWith('diff-') && f.endsWith('.md'))
    .sort();

  for (const docFile of docFiles) {
    const filePath = path.join(DOCS_DIR, docFile);
    const content = fs.readFileSync(filePath, 'utf8');
    let match;
    const re = new RegExp(rulePatternGlobal.source, rulePatternGlobal.flags);
    while ((match = re.exec(content)) !== null) {
      const ruleId = match[1].trim();
      const anchor = ruleId.toLowerCase().replace(/[^a-z0-9-]/g, '-');
      entries.push({ ruleId, docFile, anchor });
    }
  }

  // Sort deterministically by rule_id
  entries.sort((a, b) => a.ruleId.localeCompare(b.ruleId));
  return entries;
}

// ---------------------------------------------------------------------------
// Step 2: Load fixture index
// ---------------------------------------------------------------------------

/**
 * Returns a map from rule_family → array of { scenario_id, fixture_file }
 */
function loadFixtureIndex() {
  if (!fs.existsSync(FIXTURE_INDEX)) {
    console.error(`[generate-diff-traceability] ERROR: fixture index not found: ${FIXTURE_INDEX}`);
    process.exit(1);
  }
  const raw = fs.readFileSync(FIXTURE_INDEX, 'utf8');
  const index = JSON.parse(raw);

  const familyMap = {};
  for (const fixture of index.fixtures) {
    if (!familyMap[fixture.rule_family]) {
      familyMap[fixture.rule_family] = [];
    }
    familyMap[fixture.rule_family].push({
      scenario_id: fixture.scenario_id,
      fixture_file: fixture.fixture_file
    });
  }
  return familyMap;
}

// ---------------------------------------------------------------------------
// Step 3: Build traceability matrix
// ---------------------------------------------------------------------------

/**
 * Match rule_ids from docs to fixture rule_family values.
 * A rule_id matches a rule_family when the rule_id starts with or equals the rule_family,
 * or the rule_family starts with the rule_id (prefix matching for compound rule IDs).
 */
function matchRuleToFamily(ruleId, familyMap) {
  const lrule = ruleId.toLowerCase();
  for (const family of Object.keys(familyMap)) {
    if (lrule === family || lrule.startsWith(family + '-') || family.startsWith(lrule + '-')) {
      return family;
    }
  }
  return null;
}

// ---------------------------------------------------------------------------
// Step 4: Render output
// ---------------------------------------------------------------------------

function renderMatrix(ruleEntries, familyMap) {
  const lines = [];
  lines.push('# Diff Traceability Matrix');
  lines.push('');
  lines.push('> Auto-generated by `npm run generate:diff-traceability`. Do not edit manually.');
  lines.push('');
  lines.push('## Rule Coverage');
  lines.push('');

  if (ruleEntries.length === 0) {
    lines.push('_No `## Rule: {rule_id}` headings found in `docs/diff-*.md`. Add rule headings to enable traceability._');
    lines.push('');
  } else {
    lines.push('| rule_id | doc_anchor | fixture_ids | notes |');
    lines.push('|---------|-----------|-------------|-------|');
    for (const entry of ruleEntries) {
      const matchedFamily = matchRuleToFamily(entry.ruleId, familyMap);
      const fixtures = matchedFamily ? familyMap[matchedFamily] : [];
      const fixtureIds = fixtures.map(f => f.scenario_id).join(', ') || '—';
      const docRef = `${entry.docFile}#${entry.anchor}`;
      const notes = matchedFamily ? `→ ${matchedFamily}` : 'no fixture match';
      lines.push(`| ${entry.ruleId} | ${docRef} | ${fixtureIds} | ${notes} |`);
    }
    lines.push('');
  }

  // Fixture families not matched by any rule
  lines.push('## Fixture Rule Families');
  lines.push('');
  const allFamilies = Object.keys(familyMap).sort();
  lines.push('| rule_family | fixtures |');
  lines.push('|-------------|---------|');
  for (const family of allFamilies) {
    const count = familyMap[family].length;
    lines.push(`| ${family} | ${count} fixtures |`);
  }
  lines.push('');

  // Unmatched rules (rule_ids from docs with no matching fixture entry)
  const unmatchedRules = ruleEntries
    .filter(e => matchRuleToFamily(e.ruleId, familyMap) === null)
    .map(e => e.ruleId);

  lines.push('## Unmatched Rules');
  lines.push('');
  if (unmatchedRules.length === 0) {
    lines.push('_All rule IDs from docs have a matching fixture rule_family._');
  } else {
    lines.push('The following rule IDs appear in docs but have no matching fixture:');
    lines.push('');
    for (const r of unmatchedRules) {
      lines.push(`- ${r}`);
    }
  }
  lines.push('');

  lines.push(`_Generated: ${new Date().toISOString().slice(0, 10)}_`);
  lines.push('');

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  console.log('[generate-diff-traceability] scanning docs/diff-*.md for Rule: headings...');
  const ruleEntries = extractRuleIdsFromDocs();
  console.log(`[generate-diff-traceability] found ${ruleEntries.length} rule ID(s)`);

  console.log('[generate-diff-traceability] loading fixture index...');
  const familyMap = loadFixtureIndex();
  console.log(`[generate-diff-traceability] found ${Object.keys(familyMap).length} rule family families: ${Object.keys(familyMap).sort().join(', ')}`);

  const output = renderMatrix(ruleEntries, familyMap);

  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, output, 'utf8');
  console.log(`[generate-diff-traceability] wrote ${OUTPUT_FILE}`);

  const unmatchedCount = ruleEntries.filter(e => matchRuleToFamily(e.ruleId, familyMap) === null).length;
  if (unmatchedCount > 0) {
    console.log(`[generate-diff-traceability] WARNING: ${unmatchedCount} rule(s) have no fixture coverage`);
  }
}

main();
